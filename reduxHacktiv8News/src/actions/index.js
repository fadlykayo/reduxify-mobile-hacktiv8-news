export const setSearchKey = (searchKey) => {
  return {
    type: 'SET_SEARCH_KEY',
    payload: setSearchKey
  }
}

export const setPeoples = (peoples) => {
  return {
    type: 'SET_PEOPLES',
    payload: peoples
  }
}

export const setNews = (news) => {
  return {
    type: 'SET_NEWS',
    payload: news
  }
}
